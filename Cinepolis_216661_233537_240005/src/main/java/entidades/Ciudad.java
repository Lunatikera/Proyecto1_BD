package entidades;

/**
 *
 * @author Usuario
 */
public class Ciudad {
    private int id;
    private String nombre;

    public Ciudad(int ciudadId, String nombre) {
        this.id=ciudadId;
        this.nombre = nombre;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    @Override
    public String toString() {
        return "Ciudad{" + "id=" + id + ", nombre=" + nombre + '}';
    }
    
    
    
}
